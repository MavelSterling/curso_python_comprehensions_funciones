'''
Lista: es una colección ordenada y mutable de elementos que pueden ser de diferentes tipos de datos. 
    Se pueden agregar, eliminar y modificar elementos de la lista. 
    Las listas se definen utilizando corchetes [ ] y los elementos se separan por comas.
    
'''

mi_lista = [1, 'Hola', 2.5, True]


'''
tupla: es similar a una lista en que también es una colección ordenada de elementos, pero a diferencia de las listas, 
    las tuplas son inmutables, lo que significa que no se pueden modificar una vez creadas. 
    Las tuplas se definen utilizando paréntesis ( ) y los elementos se separan por comas.
    
'''
mi_tupla = (1, 'Hola', 2.5, True)


'''
Conjunto (set): es una colección no ordenada de elementos únicos y no indexados. Los elementos de un conjunto deben ser inmutables. 
    Los conjuntos se definen utilizando llaves { } o la función set() y los elementos se separan por comas.
    
'''

mi_set = {1, 'Hola', 2.5, True}
